type Messages = typeof messages.en;
type DotPrefix<TPrefix extends string, TKey extends string> = `${TPrefix}.${TKey}`;
type LeafPaths<T> = {
  [K in keyof T & string]: T[K] extends string ? K : DotPrefix<K, LeafPaths<T[K]>>;
}[keyof T & string];

export type MessageKey = LeafPaths<Messages>;

function readMessage(locale: Locale, key: MessageKey): string {
  const parts = key.split(".");
  let current: unknown = messages[locale];
  for (const part of parts) {
    if (!current || typeof current !== "object" || !(part in current)) {
      return key;
    }
    current = (current as Record<string, unknown>)[part];
  }
  return typeof current === "string" ? current : key;
}

export function translate(
  locale: Locale,
  key: MessageKey,
  values?: Record<string, string | number>
) {
  let value = readMessage(locale, key);
  if (!values) return value;

  for (const [name, replacement] of Object.entries(values)) {
    value = value.replaceAll(`{${name}}`, String(replacement));
  }
  return value;
}

